places_to_visit = ["San Francisco", "Buenos Aires", "Tokyo", "Barcelona", "New York"]
print(places_to_visit)

# print sorted array without changing its order
print(sorted(places_to_visit))
print(places_to_visit)

# reverse array order
places_to_visit.reverse()
print(places_to_visit)

# put array back in its original order
places_to_visit.reverse()
print(places_to_visit)

# sort array in place
places_to_visit.sort()
print(places_to_visit)

# sort array in reversed order
places_to_visit.sort(reverse=True)
print(places_to_visit)
